public class Hobbies {
    String fútbol, música;
    public Hobbies(String fútbol, String música) {
        this.fútbol = fútbol;
        this.música = música;
    }
    public String getFútbol() {
        return fútbol;
    }
    public void setFútbol(String fútbol) {
        this.fútbol = fútbol;
    }
    public String getMúsica() {
        return música;
    }
    public void setMúsica(String música) {
        this.música = música;
    }
}
